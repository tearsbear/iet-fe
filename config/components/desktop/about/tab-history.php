            To support the acceleration of energy transition in Indonesia, the Government of the Republic of Indonesia
            through the Decision of Minister of Maritime Affairs and Investment No. 144 of 2023 has established the
            National Energy Transition Task Force (Satgas TEN), to oversee Indonesia's energy transition process to
            achieve the Net-Zero Emission (NZE) target by 2060. One of the efforts to support their dailytasks,
            especially in the powersector, Satgas TEN formed the Supporting Task Force in the Power Sector officially
            launchedthe Indonesia Energy Transition Implementation Joint Office (IET) on January 17, 2024, located at
            Rumah PATEN, Jalan Tirtayasa IX No. 1 South Jakarta.
